import Head from "next/head";
import { magicClient } from "../lib/magic";
import { useRouter } from "next/router";
import { useState } from "react";
import styled from "styled-components";
import Link from "next/link";
import Script from "next/script";

export default function Layout({ children }) {
  const Router = useRouter();

  const logout = () => {
    magicClient.user.logout().then(async (test) => {
      console.log(await magicClient.user.isLoggedIn()); // => `false`
      const res = await fetch("/api/logout", {
        method: "GET",
      });
      Router.push("/login-magic");
    });
  };

  const [userMenu, setUserMenu] = useState(false);

  async function test() {
    const userMagic = await magicClient.user.isLoggedIn(); // => `false`

    if (userMagic) {
      // show user menu
      setUserMenu(true);
    } else {
      // hide user menu
      setUserMenu(false);
    }
  }

  test();

  return (
    <>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <Script
        src="https://upload-widget.cloudinary.com/global/all.js"
        type="text/javascript"
      ></Script>
      <Link href="/">
        <Logo src="/logo-3.png" />
      </Link>
      {userMenu ? (
        <UserMenu>
          <button onClick={logout}>Logout</button>
          <Link href="/profile">My StreetWear</Link>
        </UserMenu>
      ) : (
        <UserMenu>
          <Link href="/login-magic">login / signup</Link>
        </UserMenu>
      )}
      <Main>{children}</Main>
    </>
  );
}

const UserMenu = styled.div`
  position: fixed;
  top: 15px;
  right: 15px;
  display: grid;
  grid-row-gap: 10px;
  button {
    padding: 10px;
    background: none;
    border: 1px solid #000;
    font-size: 16px;
    &:hover {
      cursor: pointer;
    }
  }
  a {
    border: 1px solid #000;
    padding: 10px;
    font-size: 16px;
  }
`;

const Logo = styled.img`
  position: fixed;
  top: 0px;
  left: 0px;
  &:hover {
    cursor: pointer;
  }
`;

const Main = styled.main`
  margin-top: 50px;
`;
